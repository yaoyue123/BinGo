#!/usr/bin/env bash
# SessionStart hook for BinGo plugin

set -euo pipefail

# Determine plugin root directory
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]:-$0}")" && pwd)"
PLUGIN_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"

# Read bingo main skill content
bingo_content=$(cat "${PLUGIN_ROOT}/SKILL.md" 2>&1 || echo "Error reading bingo skill")

# Escape outputs for JSON using pure bash
escape_for_json() {
    local input="$1"
    local output=""
    local i char
    for (( i=0; i<${#input}; i++ )); do
        char="${input:$i:1}"
        case "$char" in
            $'\\') output+='\\' ;;
            '"') output+='\"' ;;
            $'\n') output+='\n' ;;
            $'\r') output+='\r' ;;
            $'\t') output+='\t' ;;
            *) output+="$char" ;;
        esac
    done
    printf '%s' "$output"
}

bingo_escaped=$(escape_for_json "$bingo_content")

# Output context injection as JSON
cat <<EOF
{
  "hookSpecificOutput": {
    "hookEventName": "SessionStart",
    "additionalContext": "<EXTREMELY_IMPORTANT>\nYou have BinGo - Binary vulnerability mining skills.\n\n**Below is the full content of your main bingo skill:**\n\n${bingo_escaped}\n\n</EXTREMELY_IMPORTANT>"
  }
}
EOF

exit 0
